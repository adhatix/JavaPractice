class Powers{

    public static void main(String args[]){
        double n = 3.5;
        System.out.println("the number is " + n);
        System.out.println(n + " squared is " + n * n);
        System.out.println(n + " cubed is " + n * n * n);
        System.out.println(n + " to the fourth is " + Math.pow(n, 4));
    }
}
